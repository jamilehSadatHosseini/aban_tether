class Coin {
  final int id;
  final String name;
  final double price;
  final String symbol;
  final String iconUrl;
  final bool isFavorite;

  Coin({
    required this.id,
    required this.name,
    required this.price,
    required this.symbol,
    required this.iconUrl,
    required this.isFavorite,
  });
}
